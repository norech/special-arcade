namespace arc {

/**
 * @brief Common - The color codes.
 * @details An enum with common color codes that can be used.
 */
enum ColorCode {
    BLACK = 0x000000,
    WHITE = 0xFFFFFF,
    RED = 0xFF0000,
    GREEN = 0x00FF00,
    BLUE = 0x0000FF,
    YELLOW = 0xFFFF00,
    CYAN = 0x00FFFF,
    MAGENTA = 0xFF00FF
};

}  // namespace arc
